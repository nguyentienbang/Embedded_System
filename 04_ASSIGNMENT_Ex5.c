/******************************************************************************
 * File: stack_ext.c
 ******************************************************************************/

#include <stdio.h>

#define MAX 100

int stack[MAX];
int top = -1;

/******************************************************************************
 * Function: push
 ******************************************************************************/
void push(int val)
{
    stack[++top] = val;
}

/******************************************************************************
 * Function: pop
 ******************************************************************************/
int pop(void)
{
    return stack[top--];
}

/******************************************************************************
 * Function: print_top
 ******************************************************************************/
void print_top(void)
{
    if(top >= 0)
        printf("Top = %d\n", stack[top]);
}

/******************************************************************************
 * Function: duplicate
 ******************************************************************************/
void duplicate(void)
{
    if(top >= 0)
        push(stack[top]);
}

/******************************************************************************
 * Function: swap
 ******************************************************************************/
void swap(void)
{
    int temp;
    if(top >= 1)
    {
        temp = stack[top];
        stack[top] = stack[top-1];
        stack[top-1] = temp;
    }
}

/******************************************************************************
 * Function: clear
 ******************************************************************************/
void clear(void)
{
    top = -1;
}