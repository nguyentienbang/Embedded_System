#include <stdio.h>
#include <limits.h>

/******************************************************************************
 * Function: reverse
 ******************************************************************************/
void reverse(char s[])
{
    int i, j;
    char temp;

    for(i = 0, j = strlen(s)-1; i < j; i++, j--)
    {
        temp = s[i];
        s[i] = s[j];
        s[j] = temp;
    }
}

/******************************************************************************
 * Function: itoa
 ******************************************************************************/
void itoa(int n, char s[])
{
    int i = 0;
    unsigned int num;

    if(n < 0)
        num = - (unsigned int)n;
    else
        num = n;

    do
    {
        s[i++] = num % 10 + '0';
        num /= 10;
    } while(num > 0);

    if(n < 0)
        s[i++] = '-';

    s[i] = '\0';

    reverse(s);
}
